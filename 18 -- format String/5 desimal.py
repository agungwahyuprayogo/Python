angka = 1209.09129837198
print(f"{angka}")
# bisa sebenernya make gitu doang

# tapi bagaimana kalo pengen nampilin abis koma 2 angka doang?
print(f"{angka:.2f}") 
# jadi kalo make format diatas, koma yang ditampilin setelah koma hanya 2 angka
print(f"{angka:.4f}") 
# ya kurleb kek diatas, kali ini nampilin 4 angka ke kanan setelah koma