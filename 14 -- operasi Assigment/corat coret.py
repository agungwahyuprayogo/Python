print(f"nilai agung diawal adalah {agung}")

agung += 2
print(f"setelah += 2 hasilnya {agung}")

agung -= 2
print(f"setelah -= 2 hasilnya {agung}")

agung /= 2
print(f"setelah /= 2 hasilnya {agung}")

agung *= 2
print(f"setelah *= 2 hasilnya {agung}")

