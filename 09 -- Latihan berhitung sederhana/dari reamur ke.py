# dari reamur ke celcius rumus nya 5/4 * reamur
# dari reamur ke fahrenheit rumusnya 9/4 * reamur + 32
# dari reamur ke kelvin 5/4 * reamur + 273

reamur = float(input("masukan suhu reamur : "))

celcius = (5/4) * reamur
print("dalam celcius",celcius,"celcius")

fahrenheit = ((9/4) * reamur) + 32
print("dalam fahrenheit",fahrenheit,"fahrenheit")

kelvin = ((5/4) * reamur) + 273
print("dalam kelvin",kelvin,"kelvin")