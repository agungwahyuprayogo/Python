# tipe data angka (interger) : bilangan bulat
interger = 10
print("nilai : ", interger)
print("bertype : ", type(interger))

# tipe data angka dengan koma (float)
# sebagai tanda koma , dikasih titik
float = 10.5
print("nilai : ", float)
print("bertype : ", type(float))

# tipe data string (kumpulan data) : sekumpulan huruf
ini_string = "Agoenk" 
print("nilai : ", ini_string)
print("bertype : ", type(ini_string))

# tipe data boolean (kumpulan data) : benar atau salah
ini_boolean = True
print("nilai : ", ini_boolean)
print("bertype : ", type(ini_boolean))

